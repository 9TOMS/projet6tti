            <h3>Sécurité et organisation :</h3>
<br>
1. Les propriétaires des infrastructures et des chevaux de Crépuscule (Jean-Pol
et Marie-Hélène) pourront désigner des personnes majeures pour les
remplacer dans les activités (ci-après dénommés les « référents »).
<br>
2. Toute manipulation (mettre un licol, sortir un cheval, sortir à pied, monter en
piste ou en balade) doit se faire sous la supervision des propriétaires ou en
leur absence d’un référent.
<br>
3. Demander l’autorisation à Marie-Hélène au minimum 24h à l’avance pour
venir manipuler ou monter un cheval et mentionner le nom du cheval et du
référent qui sera présent (+ obligation de prévenir en cas d’annulation
minimum 24h à l’avance sous peine de facturation de la séance réservée).
<br>
4. Le port d’une bombe d’équitation est obligatoire (un prêt de bombe par
Crépuscule est possible dans les mesures du stock disponible). Le port de
chaussures ou bottines fermées ou de bottes d’équitation ainsi que d’un
pantalon est indispensable pour monter à cheval. L’utilisation d’éperons est
interdite.
<br>
5. Les propriétaires et les référents devront s’assurer que les membres montent
des chevaux différents (pas toujours le même cheval). Cette alternance se
fera sur proposition des propriétaires ou des référents en fonction du niveau
du cavalier et des chevaux disponibles aux écuries.
<br>
6. En cas de participation à des activités par un ou des non-membres, le référent
en charge de ce ou ces non-membres est responsable de collecter le prix de
la séance ou de la balade et devra soit remettre la somme perçue directement
au trésorier soit le verser immédiatement sur le compte bancaire de
Crépuscule.
<br>
7. UNIQUEMENT les propriétaires et leurs référents sont autorisés à manipuler
les poulains. Cette règle s’applique également au cas par cas lorsqu’un cheval
présente un problème ponctuel à résoudre.
<br>
<h3>Assurance :</h3> 
<br>
8. Obligation de souscrire à une assurance responsabilité civile (RC) familiale ou
vie privée et informer la compagnie d’assurance de la pratique de l’équitation
(Fournir le nom de la compagnie et le numéro de police ainsi que,
annuellement, la preuve de paiement de la prime d’assurance).
<br>
9. Prendre une licence annuelle (licence de loisir L01) auprès de la LEWB ou
FFE pour couvrir les accidents liés à la pratique équestre. Cette licence pourra
être prise par l’intermédiaire de Crépuscule ou auprès d’un autre centre
équestre. Dans ce dernier cas vous devrez fournir une copie de votre carte de
licence.
<br> 
<h3>Rangement et propreté des lieux :</h3>
<br>
10. Ne pas laisser la porte de la sellerie grande ouverte avant de partir en balade
